import java.util.Scanner;

public class Answer10869 {
	public static void main(String args[]) {
		//10869번 사칙연산 답안
		Scanner s=new Scanner(System.in);
		int a=s.nextInt();
		int b=s.nextInt();
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
	}
}
